package com.example.demo;

public class Employee {
    int id,salary;
    String name;

    public void setSalary(int salary) {
        this.salary = salary;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }


    public String getName() {
        return name;
    }


    public int getSalary() {
        return salary;
    }
    
    
}
